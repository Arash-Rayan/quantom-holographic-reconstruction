# --- Model: Dense blocks + ASPP + Cat.Conv. ---
class DenseBlock(nn.Module):
    def __init__(self, in_ch, growth_rate=32, layers=3):
        super().__init__(); mods=[]; ch=in_ch
        for _ in range(layers):
            mods += [nn.Conv2d(ch, growth_rate, 3, padding=1, bias=False),
                     nn.BatchNorm2d(growth_rate), nn.ReLU(inplace=True)]
            ch += growth_rate
        self.layers = nn.ModuleList(mods); self.layers_n = layers
    def forward(self, x):
        feats=[x]
        for i in range(self.layers_n):
            conv,bn,act = self.layers[3*i:3*i+3]
            out = act(bn(conv(torch.cat(feats, dim=1)))); feats.append(out)
        return torch.cat(feats, dim=1)

class ASPP(nn.Module):
    def __init__(self, in_ch, out_ch, rates=(1,6,12,18)):
        super().__init__()
        self.branches = nn.ModuleList([nn.Conv2d(in_ch, out_ch, 1, padding=0, bias=False)] +
                                      [nn.Conv2d(in_ch, out_ch, 3, padding=r, dilation=r, bias=False) for r in rates[1:]])
        self.proj = nn.Conv2d(out_ch*len(self.branches), out_ch, 1, bias=False)
        self.bn = nn.BatchNorm2d(out_ch); self.act = nn.ReLU(inplace=True)
    def forward(self, x):
        y = torch.cat([b(x) for b in self.branches], dim=1)
        return self.act(self.bn(self.proj(y)))

class QHUPNet(nn.Module):
    def __init__(self, width=32):
        super().__init__()
        self.stem = nn.Sequential(nn.Conv2d(1, width, 3, padding=1, bias=False),
                                  nn.BatchNorm2d(width), nn.ReLU(inplace=True))
        self.db1 = DenseBlock(width, growth_rate=width//2, layers=3); ch1 = width + 3*(width//2)
        self.down1 = nn.Conv2d(ch1, width*2, 3, stride=2, padding=1, bias=False)
        self.db2 = DenseBlock(width*2, growth_rate=width//2, layers=3); ch2 = width*2 + 3*(width//2)
        self.down2 = nn.Conv2d(ch2, width*4, 3, stride=2, padding=1, bias=False)
        self.db3 = DenseBlock(width*4, growth_rate=width//2, layers=3); ch3 = width*4 + 3*(width//2)
        self.aspp = ASPP(ch3, width*4)
        self.up1 = nn.ConvTranspose2d(width*4, width*2, 2, stride=2)
        self.dec1 = nn.Sequential(nn.Conv2d(width*2 + ch2, width*2, 3, padding=1, bias=False),
                                  nn.BatchNorm2d(width*2), nn.ReLU(inplace=True))
        self.up2 = nn.ConvTranspose2d(width*2, width, 2, stride=2)
        self.dec2 = nn.Sequential(nn.Conv2d(width + ch1, width, 3, padding=1, bias=False),
                                  nn.BatchNorm2d(width), nn.ReLU(inplace=True))
        self.head_amp = nn.Sequential(nn.Conv2d(width, 1, 1), nn.Sigmoid())
        self.head_phase = nn.Conv2d(width, 1, 1)
    def forward(self, x):
        x0=self.stem(x); e1=self.db1(x0); x1=self.down1(e1); e2=self.db2(x1); x2=self.down2(e2)
        b=self.db3(x2); b=self.aspp(b); d1=self.up1(b); d1=torch.cat([d1,e2],dim=1); d1=self.dec1(d1)
        d2=self.up2(d1); d2=torch.cat([d2,e1],dim=1); d2=self.dec2(d2)
        amp=self.head_amp(d2); phase=math.pi*torch.tanh(self.head_phase(d2))
        return amp, phase

