# --- Losses & metrics ---
class PhaseL1(nn.Module):
    def forward(self, pred, target):
        diff = torch.atan2(torch.sin(pred - target), torch.cos(pred - target))
        return diff.abs().mean()

ssim_torchmetrics = StructuralSimilarityIndexMeasure(data_range=1.0).to(device)  # keep metric on GPU/CPU with tensors
ssim_loss = SSIM(data_range=1.0, size_average=True, channel=1)

def amp_loss_fn(pred_amp, gt_amp):
    return (1.0 - ssim_loss(pred_amp, gt_amp)) + F.l1_loss(pred_amp, gt_amp)

phase_loss_fn = PhaseL1()

# --- Training/eval setup ---
BATCH = 32
train_ds = QHUPMnist(train=True, size=64, n_samples=4000)
val_ds   = QHUPMnist(train=False, size=64, n_samples=512)
train_dl = DataLoader(train_ds, batch_size=BATCH, shuffle=True, num_workers=2 if torch.cuda.is_available() else 0)
val_dl   = DataLoader(val_ds, batch_size=BATCH, shuffle=False, num_workers=2 if torch.cuda.is_available() else 0)

optimizer = torch.optim.AdamW(model.parameters(), lr=2e-3, weight_decay=1e-4)
scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=10)
EPOCHS = 5  # bump to 50-100 for stronger results

train_log = []  # to save CSV/JSON later

def run_epoch(loader, train=True):
    model.train(train)
    total_loss = 0.0
    total_ssim = 0.0
    n = 0
    for batch in loader:
        holo = batch["holo"].to(device)
        if holo.ndim == 5:  # defensive: [B,1,1,H,W] -> [B,1,H,W]
            holo = holo.squeeze(1)
        gt_amp = batch["amp"].to(device)
        gt_phase = batch["phase"].to(device)

        if train:
            optimizer.zero_grad()
        pred_amp, pred_phase = model(holo)
        loss = amp_loss_fn(pred_amp, gt_amp) + 0.5 * phase_loss_fn(pred_phase, gt_phase)
        if train:
            loss.backward()
            optimizer.step()

        with torch.no_grad():
            ssim_val = ssim_torchmetrics(pred_amp, gt_amp)

        bs = holo.size(0)
        total_loss += float(loss.item()) * bs
        total_ssim += float(ssim_val.item()) * bs
        n += bs

    return total_loss / n, total_ssim / n

best_val = float("inf")
for ep in range(1, EPOCHS+1):
    tr_loss, tr_ssim = run_epoch(train_dl, train=True)
    val_loss, val_ssim = run_epoch(val_dl, train=False)
    scheduler.step()

    train_log.append({"epoch": ep, "train_loss": tr_loss, "train_ssim": tr_ssim,
                      "val_loss": val_loss, "val_ssim": val_ssim, "lr": scheduler.get_last_lr()[0]})
    print(f"Epoch {ep:02d} | train loss {tr_loss:.4f} SSIM {tr_ssim:.3f} | val loss {val_loss:.4f} SSIM {val_ssim:.3f}")

    if val_loss < best_val:
        best_val = val_loss
        torch.save(model.state_dict(), OUT/"qhupnet_best.pt")

