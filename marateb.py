# --- Resolution sweep (CSV + plot PNG) ---
def make_sine_grating(H, W, cycles):
    x = torch.linspace(0, 2*math.pi*cycles, W).view(1,1,1,W).expand(1,1,H,W)
    y = torch.linspace(0, 2*math.pi*cycles, H).view(1,1,H,1).expand(1,1,H,W)
    return 0.5*(torch.sin(x) + torch.sin(y)) * 0.5 + 0.5

@torch.no_grad()
def resolution_sweep(cycles_list=(2,4,6,8,10,12,14)):
    model.eval(); rows=[]
    for c in cycles_list:
        amp = make_sine_grating(64,64,c); phase = torch.zeros_like(amp)
        params = random_params(visibility=0.5, read_noise_std=0.01, background_level=0.01)
        holo = simulate_qhup_hologram(amp, phase, params).to(device)
        if holo.ndim == 5: holo = holo.squeeze(1)
        pred_amp, _ = model(holo)
        ssim_val = ssim_torchmetrics(pred_amp, amp.to(device)).item()
        rows.append({"cycles_per_64px": int(c), "ssim_amp": ssim_val})
        print(f"cycles={c:2d}  SSIM={ssim_val:.3f}")
    return rows

