# --- Robust montage builder (handles grayscale, RGBA) ---
def _load_rgb_image(path):
    import imageio.v2 as imageio
    im = imageio.imread(str(path))
    # Grayscale -> RGB
    if im.ndim == 2:
        im = np.stack([im]*3, axis=-1)
    # RGBA -> RGB
    elif im.ndim == 3 and im.shape[2] == 4:
        im = im[..., :3]
    # Normalize to uint8 if needed
    if im.dtype != np.uint8:
        im = (255 * (im - im.min()) / (im.ptp() + 1e-8)).astype(np.uint8)
    return im

def make_montage(panel_paths, cols=3, save_path=OUT/"montages/montage_3x3.png"):
    import imageio.v2 as imageio
    imgs = [_load_rgb_image(p) for p in panel_paths]
    rows = (len(imgs) + cols - 1) // cols
    h = max(im.shape[0] for im in imgs)
    w = max(im.shape[1] for im in imgs)
    montage = np.ones((rows*h, cols*w, 3), dtype=np.uint8) * 255
    for i, im in enumerate(imgs):
        r, c = divmod(i, cols)
        canvas = np.ones((h, w, 3), dtype=np.uint8) * 255
        ih, iw = im.shape[:2]
        canvas[:ih, :iw, :] = im
        montage[r*h:(r+1)*h, c*w:(c+1)*w, :] = canvas
    imageio.imwrite(str(save_path), montage)
    return save_path

