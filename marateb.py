@torch.no_grad()
def evaluate_vs_visibility(num=200, vis_list=(0.2,0.4,0.6,0.8)):
    model.eval(); results=[]
    for vis in vis_list:
        amps, phases, holos = [], [], []
        for _ in range(num):
            amp, phase = make_random_amplitude_phase(1, 64, 64)
            params = random_params(visibility=vis, read_noise_std=0.02, background_level=0.02)
            holo = simulate_qhup_hologram(amp, phase, params)
            amps.append(amp); phases.append(phase); holos.append(holo)
        amp = torch.cat(amps,0).to(device)
        phase = torch.cat(phases,0).to(device)
        holo = torch.cat(holos,0).to(device)
        if holo.ndim == 5: holo = holo.squeeze(1)
        pred_amp, pred_phase = model(holo)
        loss = (amp_loss_fn(pred_amp, amp) + 0.5*phase_loss_fn(pred_phase, phase)).item()
        ssim_val = ssim_torchmetrics(pred_amp, amp).item()
        results.append({"visibility": vis, "loss": loss, "ssim_amp": ssim_val})
        print(f"vis={vis:.1f}  loss={loss:.4f}  SSIM={ssim_val:.3f}")
    return results

