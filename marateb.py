# --- QHUP-like simulator (shape-safe) ---
@dataclass
class QHUPParams:
    r_p: float = 1.0; r_s: float = 1.0; r_i: float = 1.0
    gamma_mag: float = 0.6
    phi_p: float = 0.0; delta_phi: float = 0.0
    read_noise_std: float = 0.01; background_level: float = 0.0

def simulate_qhup_hologram(amplitude, phase, params: QHUPParams):
    """
    amplitude, phase: [B,1,H,W] tensors; returns [B,1,H,W]
    """
    r_p, r_s, r_i = params.r_p, params.r_s, params.r_i
    gamma = params.gamma_mag
    phi = phase - params.phi_p + params.delta_phi
    base = r_p**2 + r_s**2
    inter = 2.0 * r_p * r_s * r_i * gamma * torch.cos(phi)
    I = base + amplitude * inter
    if params.background_level != 0.0:
        I = I + params.background_level
    if params.read_noise_std > 0.0:
        I = I + torch.randn_like(I) * params.read_noise_std
    # Normalize per-frame to [0,1]
    I = I - I.amin(dim=(-2,-1), keepdim=True)
    I = I / (I.amax(dim=(-2,-1), keepdim=True) + 1e-8)
    return I

def random_params(visibility: float, read_noise_std: float, background_level: float):
    gamma_mag = float(np.clip(visibility, 0.0, 1.0))
    return QHUPParams(
        gamma_mag=gamma_mag,
        delta_phi=float(np.random.uniform(-math.pi, math.pi)),
        read_noise_std=read_noise_std,
        background_level=background_level
    )

def make_random_amplitude_phase(B, H, W):
    amp = torch.rand(B,1,H,W)
    phase = torch.randn(B,1,H,W)
    k = 9
    phase = F.avg_pool2d(phase, k, stride=k//2, padding=0)
    phase = F.interpolate(phase, size=(H,W), mode="bilinear", align_corners=False)
    phase = math.pi * torch.tanh(phase)
    return amp.clamp(0,1), phase

# --- Dataset: MNIST amplitude + synthetic phase ---
class QHUPMnist(Dataset):
    def __init__(self, train=True, size=64, n_samples=20000):
        self.size=size; self.train=train; self.n_samples=n_samples
        self.tf = transforms.Compose([transforms.Resize((size,size)), transforms.ToTensor()])
        self.mnist = datasets.MNIST(root="./data", train=train, download=True, transform=self.tf)
    def __len__(self): return self.n_samples
    def _random_phase(self, H, W):
        p = torch.randn(1,1,H,W)
        p = F.avg_pool2d(p, 9, stride=4)
        p = F.interpolate(p, size=(H,W), mode="bilinear", align_corners=False)
        return math.pi * torch.tanh(p)
    def __getitem__(self, idx):
        img,_ = self.mnist[random.randrange(0, len(self.mnist))]
        amp = img                                  # [1,H,W]
        phase = self._random_phase(self.size,self.size)  # [1,1,H,W]
        vis = np.random.uniform(0.2, 0.9)
        read_noise = np.random.uniform(0.0, 0.03)
        bg = np.random.uniform(0.0, 0.05)
        params = random_params(vis, read_noise, bg)
        holo = simulate_qhup_hologram(amp.unsqueeze(0), phase, params)[0]  # [1,H,W]
        return {"holo":holo, "amp":amp, "phase":phase.squeeze(0)}

